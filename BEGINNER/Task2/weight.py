def ton_to_kilogram():
    ton = float(input("\nTon: "))
    kilogram = ton * 10**3
    return kilogram
    
def kilogram_to_ton():
    kilogram = float(input("\nKilogram: "))
    ton = kilogram / 10**3
    return ton
    
